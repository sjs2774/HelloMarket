package follow;

public class FollowDTO {
	private Integer followIdx;
	private String followingNick;
	private String followerNick;
	public Integer getFollowIdx() {
		return followIdx;
	}
	public void setFollowIdx(Integer followIdx) {
		this.followIdx = followIdx;
	}
	public String getFollowingNick() {
		return followingNick;
	}
	public void setFollowingNick(String followingNick) {
		this.followingNick = followingNick;
	}
	public String getFollowerNick() {
		return followerNick;
	}
	public void setFollowerNick(String followerNick) {
		this.followerNick = followerNick;
	}
	
	
}
